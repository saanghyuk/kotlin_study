// 주석
// 탭(띄우기)
// 변수 var val

fun main() {
    println("Hello, world!!!")
    println("line2")
    println("line3")


    // 변수 => 값을 담아 놓는 박스
    val box1 = "test box1";
    println(box1);
    val box2 = "test box2";
    println(box2);

    var box3 = "test box3";
    println(box3)
    box3 = "new value";
    println(box3);


    // 숫자와 문자
    println("123"+ 4);
    println(123  + 4);


}
